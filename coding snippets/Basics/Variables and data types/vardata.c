#include <stdio.h>
#include <stdlib.h>

int main()
{
    // integer variable
    int a = 10;

    // float variable
    float b = 10.5;

    // double variable
    double d = 10.5;

    // character variable
    char c = 'a';

    // short variable
    short s = 10;

    // long variable
    long l = 100000000;

    // long long variable
    long long ll = 100000000;

    // unsigned variable
    unsigned u = 10;

    // string variable
    char str[10] = "hello";

    return 0;
}